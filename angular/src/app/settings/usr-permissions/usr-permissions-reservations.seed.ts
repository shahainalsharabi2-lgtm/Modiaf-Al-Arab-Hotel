import type { PermissionActionSeed, PermissionEntitySeed } from './usr-permissions.seed';

function act(label: string, id?: string): PermissionActionSeed {
  const slug =
    id ??
    label
      .trim()
      .toLowerCase()
      .replace(/[^\u0600-\u06FFa-z0-9]+/gi, '-')
      .replace(/^-|-$/g, '');
  return { id: slug || 'action', label };
}

function row(id: string, name: string, labels: string[]): PermissionEntitySeed {
  return { id, name, actions: labels.map((label) => act(label)) };
}

